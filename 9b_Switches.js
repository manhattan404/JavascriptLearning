// SWITCHES

const x = 11;

const color = x > 10 ? 'red' : 'blue';

switch(color) {
    case 'red': 
        console.log('The color is red');
        break;
    case 'blue':
        console.log('The color is blue');
        break;
    default:
        console.log('The color is neither red or blue!')

}



